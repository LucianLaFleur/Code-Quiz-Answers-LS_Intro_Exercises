# Q: What's the major difference between an Array and a Hash?
#
# A: An array has its items indexed in order, a hash contains key-value pairs,
# where usually a symbol or a string takes the place of an index, pointing to
# a particular value.
# Arrays are good for grouping data that makes sense to have a sequential order
# for sorting it. Hashes are better for data that can be put into various chunks
# as represented by the keys.
