What is exception handling and what problem does it solve?

An "exception" is more or less the same as an "error". When running code,
many kinds of errors can potentially occur, and "exception handling" refers
to giving instructions as to what the program should do in the event that
an error is raised.

usually, an error will cause a program to stop running where the error is
raised, but making a "rescue" clause can tell the program to do something
when the error occurs, and then keep running lines of code that come after
the problem area.

